#include "3-calc.h"

/**
 * op_add - Calculates the sum of two integers
 * @a: first integer
 * @b: Second integer
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - Cal the difference of two integers
 * @a: first integer
 * @b: second integer
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
return(a - b);
}

/**
 * op_mul - Cal the product of two integers
 * @a: First integer
 * @b: second integer
 * Return: Multiplication of a and b
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - Cal the division of two integers
 * @a: first integer
 * @b: second integer
 * Return: results of the division
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - Cal the remainder of the division of two integers
 * @a: first integer
 * @b: second integer
 * Return: remainder of a divided b
 */

int op_mod(int a, int b)
{
return (a % b);
}
